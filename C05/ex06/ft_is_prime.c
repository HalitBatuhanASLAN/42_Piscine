int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	if (nb < 2)
		return (0);
	if (nb % 2 == 0 && nb != 2)
		return (0);
	while (nb / 2 >= i)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
